import Foundation
import SwiftUI

extension Color {
    static let theme = ColorTheme()
}

struct ColorTheme {
    let primary = Color("Primary")
    let secondary = Color("Secondary")
    let tertiary = Color("Tertiary")
    let primarywhite = Color("PrimaryWhite")
    let secpurple = Color("SecPurple")
}
